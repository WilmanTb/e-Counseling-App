package com.e_counseling.e_counseling.Model;

public class SkorSiswa {

    String Nama;
    String SkorPerilaku;
    String Status;

    public String getNama() {
        return Nama;
    }

    public void setNama(String nama) {
        Nama = nama;
    }

    public String getSkorPerilaku() {
        return SkorPerilaku;
    }

    public void setSkorPerilaku(String skorPerilaku) {
        SkorPerilaku = skorPerilaku;
    }

    public String getStatus() {
        return Status;
    }

    public void setStatus(String status) {
        Status = status;
    }
}
